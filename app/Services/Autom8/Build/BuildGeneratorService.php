<?php

namespace App\Services\Autom8\Build;

use App\DTO\BuildSelectionData;
use App\Models\CatalogVersion;
use App\Models\GeneratedBuild;
use Illuminate\Support\Facades\File;
use Illuminate\Support\Facades\Log;
use Throwable;

class BuildGeneratorService
{
    public function __construct(
        protected SelectionResolver $selectionResolver,
        protected ManifestBuilder $manifestBuilder,
        protected ScriptBuilder $scriptBuilder,
        protected ZipBuilder $zipBuilder,
        protected ChecksumService $checksumService,
    ) {}

    public function generate(BuildSelectionData $selection): GeneratedBuild
    {
        Log::info('AutoM8 build generation started.', [
            'distro' => $selection->distroSlug,
            'profiles' => $selection->selectedProfileSlugs,
            'packages' => $selection->selectedPackageSlugs,
            'actions' => $selection->selectedActionSlugs,
        ]);

        try {
            $resolvedSelection = $this->selectionResolver->resolve($selection);

            $catalogVersion = CatalogVersion::query()
                ->latest('published_at')
                ->value('version');

            $manifest = $this->manifestBuilder->build(
                selection: $selection,
                resolvedSelection: $resolvedSelection,
                catalogVersion: $catalogVersion
            );

            $scriptBuild = $this->scriptBuilder->build($selection, $resolvedSelection);

            $this->scriptBuilder->writeManifest(
                $scriptBuild['manifest_path'],
                $manifest
            );

            $this->writeReadme(
                $scriptBuild['base_path'],
                $selection,
                $resolvedSelection
            );

            $zipFilePath = storage_path('app/autom8/artifacts/' . $scriptBuild['build_uuid'] . '.zip');

            $this->zipBuilder->build(
                $scriptBuild['base_path'],
                $zipFilePath
            );

            $hashSha256 = $this->checksumService->sha256($zipFilePath);

            $generatedBuild = GeneratedBuild::query()->create([
                'uuid' => $scriptBuild['build_uuid'],
                'target_distro' => $selection->distroSlug,
                'selected_profiles_json' => $selection->selectedProfileSlugs,
                'selected_packages_json' => $selection->selectedPackageSlugs,
                'selected_actions_json' => [
                    'slugs' => $selection->selectedActionSlugs,
                    'inputs' => $selection->actionInputs,
                ],
                'manifest_json' => $manifest,
                'zip_path' => $zipFilePath,
                'hash_sha256' => $hashSha256,
            ]);

            Log::info('AutoM8 build generation finished.', [
                'build_id' => $generatedBuild->id,
                'uuid' => $generatedBuild->uuid,
                'zip_path' => $generatedBuild->zip_path,
                'hash_sha256' => $generatedBuild->hash_sha256,
            ]);

            return $generatedBuild;
        } catch (Throwable $e) {
            Log::error('AutoM8 build generation failed.', [
                'distro' => $selection->distroSlug,
                'profiles' => $selection->selectedProfileSlugs,
                'packages' => $selection->selectedPackageSlugs,
                'actions' => $selection->selectedActionSlugs,
                'error' => $e->getMessage(),
            ]);

            throw $e;
        }
    }

    protected function writeReadme(string $basePath, BuildSelectionData $selection, array $resolvedSelection): void
    {
        $packageCount = count($resolvedSelection['packages'] ?? []);
        $actionCount = count($resolvedSelection['actions'] ?? []);
        $warnings = $resolvedSelection['warnings'] ?? [];
        $generatedAt = now()->toDateTimeString();

        $readme = <<<README
# AutoM8 Build Package

This package was generated by AutoM8.

## Generated at
- {$generatedAt}

## Target distro
- {$selection->distroSlug}

## Included packages
- Total packages: {$packageCount}

## Included system actions
- Total actions: {$actionCount}

## Execution
Run the following command from this directory:

```bash
sudo ./install.sh
```

## Warnings
- If you see any warnings during the build generation, they will be listed here:

README;

        if (empty($warnings)) {
            $readme .= "\n- No warnings generated.\n";
        } else {
            foreach ($warnings as $warning) {
                $readme .= "\n- {$warning}";
            }

            $readme .= "\n";
        }

        File::put($basePath . '/README.md', $readme);
    }
}
